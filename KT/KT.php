<?php
// Function to get the URL of the most recent image in img.txt
function get_image_url() {
	// Get the contents of img.txt
	$file = file_get_contents("img.txt");

	// Split the contents into an array of lines
	$lines = explode("\n", $file);

	// Remove any empty lines from the array
	$lines = array_filter($lines);

	// Reverse the array so that the most recent image is first
	$lines = array_reverse($lines);

	// Get the URL of the first line (i.e., the most recent image)
	$url = trim($lines[0]);

	// Return the URL
	return $url;
}
